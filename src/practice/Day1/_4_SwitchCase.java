package practice.Day1;

public class _4_SwitchCase {

	public static void main(String[] args) {

		char myChar = 'O';

		switch (myChar) {
		case 'A':
			System.out.println(myChar + " is a Vowel");
			break;
		case 'E':
			System.out.println(myChar + " is a Vowel");
			break;
		case 'I':
			System.out.println(myChar + " is a Vowel");
			break;
		case 'O':
			System.out.println(myChar + " is a Vowel");
			break;
		case 'U':
			System.out.println(myChar + " is a Vowel");
			break;
		default:
			System.out.println(myChar + " is Not a Vowel");
			break;
		}

	}

}
